package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		
		// "Hello"
		System.out.println("\"hello\"");
		
		// control character
		// \t: tab
		// \r: carriage return
		// \n: newline
		// \b: bell
		
		// '
		char c = '\'';
		System.out.println();
	}

}
